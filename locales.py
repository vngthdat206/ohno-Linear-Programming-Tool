def t(key: str, **kwargs: Any) -> str:
    """Trả về chuỗi văn bản theo ngôn ngữ hiện tại."""
    text = TRANSLATIONS.get(current_lang, TRANSLATIONS["vi"]).get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text

def get_theme() -> Dict[str, str]:
    """Trả về bộ màu cấu hình hiện tại."""
    return THEMES.get(current_theme, THEMES["light"])

def set_lang(lang: str) -> None:
    global current_lang
    if lang in TRANSLATIONS:
        current_lang = lang

def set_theme(theme: str) -> None:
    global current_theme
    if theme in THEMES:
        current_theme = theme
